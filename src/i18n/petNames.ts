// Build a reverse lookup: for a given locale, maps lowercase translated term → lowercase English names
const _cache: Record<string, Map<string, string[]>> = {};

export function getSearchAliasMap(locale: string): Map<string, string[]> {
    if (_cache[locale]) return _cache[locale];

    const aliases = petSearchAliases[locale];
    if (!aliases) {
        _cache[locale] = new Map();
        return _cache[locale];
    }

    // Build map: each translated term maps to one or more English names
    const map = new Map<string, string[]>();
    for (const [translated, english] of Object.entries(aliases)) {
        const key = translated.toLowerCase();
        const existing = map.get(key) || [];
        existing.push(english.toLowerCase());
        map.set(key, existing);
    }

    _cache[locale] = map;
    return _cache[locale];
}

// Check if a pet name matches a search query in a given locale
export function matchesPetSearch(petName: string, query: string, locale: string): boolean {
    const lowerName = petName.toLowerCase();
    const lowerQuery = query.toLowerCase().trim();

    // Always match English name
    if (lowerName.includes(lowerQuery)) return true;

    // For non-English locales, check if the query matches any translated alias
    if (locale !== 'en') {
        const aliasMap = getSearchAliasMap(locale);
        for (const [translatedTerm, englishNames] of aliasMap) {
            // If the user's query partially matches a translated term...
            if (translatedTerm.includes(lowerQuery) || lowerQuery.includes(translatedTerm)) {
                // ...check if the pet's English name matches any of the mapped English names
                if (englishNames.some((en) => lowerName.includes(en))) {
                    return true;
                }
            }
        }
    }

    return false;
}
